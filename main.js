var facebookLinkSelector = '[href*="l.facebook.com"],[href*="www.facebook.com/l.php"]';
var timer;

var mutationObserver = new MutationObserver(function(){
    clearTimeout(timer);
    timer = setTimeout(markLinks, 1000);
});
mutationObserver.observe(document.body, {
    childList: true,
    subtree: true
});

function markLinks(){
    //console.log("page stopped changing long enough to mark links");
    $('.userContentWrapper').has(facebookLinkSelector).each(function(ev, el){
        var link = $(el).find(facebookLinkSelector).filter(function(){return $(this).text() != "";})[0];
        if (link && !$(link).attr("bias_checked")){
            $(link).attr("bias_checked", true);
            var unfacebookedURL = unfacebookURL(link.href);
            
            if (isShort(unfacebookedURL)){
                $.getJSON('https://unshorten.me/json/'+unfacebookedURL, function(data){
                    if (data.success==true){
                        addScoreToLink(link, data.resolvedURL);
                    }
                });
            }
            else {
                addScoreToLink(link, unfacebookedURL);
            }
        }
    });
}

function unfacebookURL(facebookedURL){
    var unfacebookedURL;
    if (facebookedURL.indexOf("l.facebook.com")>-1){
        unfacebookedURL = decodeURIComponent(facebookedURL.substring(30).split('&h')[0])
    } else if (facebookedURL.indexOf("www.facebook.com/l.php")>-1){
        unfacebookedURL = decodeURIComponent(facebookedURL.substring(33).split('&h')[0])
    }
    return unfacebookedURL;
}

function isShort(url){
    return url.match(".*(buff\.ly)|(bit\.ly)|(itsh\.bo)|(hucka\.be).*");
}

